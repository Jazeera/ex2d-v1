///////////////////////////////////////////////////////////////////////////////

[AddComponentMenu("ex2D Helper/EffectTo/Move To")]
public class exMoveTo : exEffectToVector3 {
	
    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

	void Update () {
        transform.localPosition = Step();
	}

    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

    public override void Play () {
        PlayFrom ( transform.localPosition );
    }

    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

    public override void Stop () {
        if ( enabled == true )
            transform.localPosition = StopAt();
    }
}
